# if
age = 18
if age >= 18:
    print("You can vote")

#if-else 
num = 7
if num % 2 == 0:
    print("Even")
else:
    print("Odd")

# if - elif - else 
marks = 75
if marks >= 80:
    print("A+")
elif marks >= 70:
    print("A")
elif marks >= 60:
    print("A-")
else:
    print("Fail")
